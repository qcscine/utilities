GTOExpansion STO_nG::getGTOExpansion(unsigned int N, unsigned int n, unsigned int l, double exponent) {
  auto values = get(N, n, l, exponent);
  GTOExpansion gtos(N);
  gtos.setAngularMomentum(l);

  for (unsigned int i = 0; i < N; i++) {
    gtos.setGTF(i, values[i].first, values[i].second);
  }

  return gtos;
}

std::vector<STO_nG::parameterPair> STO_nG::get(unsigned int N, unsigned int n, unsigned int l, double exponent) {
  std::vector<parameterPair> parameters(N);

  std::array<double, 6> a = {};
  std::array<double, 6> c = {};
  getValues(a, c, N, n, l);
  for (unsigned int i = 0; i < N; i++) {
    parameters[i].first = a[i] * exponent * exponent;
    parameters[i].second = c[i];
  }

  return parameters;
}

